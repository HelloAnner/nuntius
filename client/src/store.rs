    pub async fn import_app_history(&self, thread_id: &str, thread: &Value) -> Result<()> {
        for (turn_index, turn) in thread
            .get("turns")
            .and_then(Value::as_array)
            .into_iter()
            .flatten()
            .enumerate()
        {
            // Commit per turn: a whole-thread transaction can hold the WAL write lock
            // for seconds on large histories and starve interactive writes.
            let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
            let app_turn = turn
                .get("id")
                .and_then(Value::as_str)
                .ok_or_else(|| anyhow!("App Server turn has no id"))?;
            let turn_id = if let Some(id) = sqlx::query_scalar::<_, String>(
                "SELECT id FROM turns WHERE thread_id=? AND app_server_turn_id=?",
            )
            .bind(thread_id)
            .bind(app_turn)
            .fetch_optional(&mut *tx)
            .await?
            {
                id
            } else {
                new_id("trn")
            };
            let status = value_status(turn.get("status"), "completed");
            let started = unix_value_to_rfc3339(turn.get("startedAt"));
            let completed = unix_value_to_rfc3339(turn.get("completedAt"));
            sqlx::query("INSERT INTO turns(id,thread_id,app_server_turn_id,ordinal,status,started_at,completed_at) VALUES(?,?,?,?,?,?,?) ON CONFLICT(id) DO UPDATE SET ordinal=excluded.ordinal,status=excluded.status,started_at=COALESCE(excluded.started_at,turns.started_at),completed_at=COALESCE(excluded.completed_at,turns.completed_at)")
                .bind(&turn_id).bind(thread_id).bind(app_turn).bind(turn_index as i64+1).bind(status).bind(&started).bind(&completed).execute(&mut *tx).await?;
            let mut claimed_item_ids = HashSet::new();
            for item in turn
                .get("items")
                .and_then(Value::as_array)
                .into_iter()
                .flatten()
            {
                let app_item = item.get("id").and_then(Value::as_str).unwrap_or("");
                let raw_kind = item
                    .get("type")
                    .and_then(Value::as_str)
                    .unwrap_or("unknown");
                let kind = normalize_item_kind(raw_kind);
                let mut existing = if app_item.is_empty() {
                    None
                } else {
                    sqlx::query_scalar::<_, String>(
                        "SELECT id FROM items WHERE turn_id=? AND app_server_item_id=?",
                    )
                    .bind(&turn_id)
                    .bind(app_item)
                    .fetch_optional(&mut *tx)
                    .await?
                };
                if existing.is_none() && kind == "user_message" {
                    existing = sqlx::query_scalar::<_, String>("SELECT id FROM items WHERE turn_id=? AND kind='user_message' AND app_server_item_id IS NULL ORDER BY ordinal,id")
                        .bind(&turn_id)
                        .fetch_all(&mut *tx)
                        .await?
                        .into_iter()
                        .find(|id| !claimed_item_ids.contains(id));
                }
                let text = app_item_text(item);
                if existing.is_none()
                    && let Some(content) = text.as_deref().filter(|t| !t.is_empty())
                {
                    // The live path records the same message under the notification's item id,
                    // which does not always match the id the history endpoint returns; fall back
                    // to a content match so backfill never duplicates a recorded message.
                    existing = sqlx::query_scalar::<_, String>(
                        "SELECT id FROM items WHERE turn_id=? AND kind=? AND content_text=? ORDER BY ordinal,id",
                    )
                    .bind(&turn_id)
                    .bind(&kind)
                    .bind(content)
                    .fetch_all(&mut *tx)
                    .await?
                    .into_iter()
                    .find(|id| !claimed_item_ids.contains(id));
                }
                let item_id = existing.unwrap_or_else(|| new_id("itm"));
                claimed_item_ids.insert(item_id.clone());
                let stamp = app_item_occurred_at(item)
                    .or_else(|| started.clone())
                    .or_else(|| completed.clone())
                    .unwrap_or_else(now);
                // New rows take the next free ordinal instead of the app-side index: live-recorded
                // items (user/agent messages) already occupy ordinals, and the (turn_id, ordinal)
                // unique index must never abort the whole history import. Existing rows keep their
                // ordinal so re-imports cannot reshuffle what the live path already persisted.
                sqlx::query("INSERT INTO items(id,turn_id,app_server_item_id,ordinal,kind,status,revision,content_text,structured_detail,occurred_at,completed_at) VALUES(?,?,NULLIF(?,''),COALESCE((SELECT MAX(ordinal)+1 FROM items WHERE turn_id=?),1),?,'completed',1,?,?,?,?) ON CONFLICT(id) DO UPDATE SET app_server_item_id=COALESCE(excluded.app_server_item_id,items.app_server_item_id),kind=excluded.kind,content_text=excluded.content_text,structured_detail=excluded.structured_detail,occurred_at=CASE WHEN julianday(excluded.occurred_at)<julianday(items.occurred_at) THEN excluded.occurred_at ELSE items.occurred_at END,completed_at=COALESCE(excluded.completed_at,items.completed_at),revision=CASE WHEN items.app_server_item_id IS NOT COALESCE(excluded.app_server_item_id,items.app_server_item_id) OR items.kind IS NOT excluded.kind OR items.content_text IS NOT excluded.content_text OR items.structured_detail IS NOT excluded.structured_detail OR (excluded.completed_at IS NOT NULL AND items.completed_at IS NOT excluded.completed_at) THEN items.revision+1 ELSE items.revision END")
                    .bind(&item_id).bind(&turn_id).bind(app_item).bind(&turn_id).bind(kind).bind(text).bind(serde_json::to_string(item)?).bind(&stamp).bind(&completed).execute(&mut *tx).await?;
                if kind == "user_message" {
                    for path in app_item_local_image_paths(item) {
                        let attachment_id: Option<String> = sqlx::query_scalar(
                            "SELECT id FROM attachments WHERE thread_id=? AND local_path=?",
                        )
                        .bind(thread_id)
                        .bind(path)
                        .fetch_optional(&mut *tx)
                        .await?;
                        if let Some(attachment_id) = attachment_id {
                            sqlx::query("INSERT OR IGNORE INTO item_attachments(item_id,attachment_id,ordinal) VALUES(?,?,COALESCE((SELECT MAX(ordinal)+1 FROM item_attachments WHERE item_id=?),1))")
                                .bind(&item_id).bind(attachment_id).bind(&item_id)
                                .execute(&mut *tx).await?;
                        }
                    }
                }
            }
            tx.commit().await?;
        }
        // `thread/read` is the upstream snapshot. Persist its thread status and
        // collapse contradictory historical turn flags before exposing the
        // snapshot to either frontend.
        let stamp = now();
        let thread_status = value_status(thread.get("status"), "idle");
        let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
        if thread_status == "active" {
            let current: Option<String> = sqlx::query_scalar(
                "SELECT id FROM turns WHERE thread_id=? AND status IN ('active','running','inProgress') ORDER BY ordinal DESC LIMIT 1",
            )
            .bind(thread_id)
            .fetch_optional(&mut *tx)
            .await?;
            if let Some(current) = current {
                sqlx::query("UPDATE turns SET status='unknown',completed_at=COALESCE(completed_at,?) WHERE thread_id=? AND id<>? AND status IN ('active','running','inProgress')")
                    .bind(&stamp)
                    .bind(thread_id)
                    .bind(current)
                    .execute(&mut *tx)
                    .await?;
            }
        } else {
            sqlx::query("UPDATE turns SET status='unknown',completed_at=COALESCE(completed_at,?) WHERE thread_id=? AND status IN ('active','running','inProgress')")
                .bind(&stamp)
                .bind(thread_id)
                .execute(&mut *tx)
                .await?;
        }
        sqlx::query("UPDATE threads SET status=?,updated_at=? WHERE id=?")
            .bind(thread_status)
            .bind(&stamp)
            .bind(thread_id)
            .execute(&mut *tx)
            .await?;
        tx.commit().await?;
        Ok(())
    }
    pub async fn history_records(
        &self,
        thread_id: &str,
        device_id: &str,
    ) -> Result<Vec<HistoryRecord>> {
        let mut records = Vec::new();
        if let Some(thread) = self.thread(thread_id, device_id).await? {
            records.push(HistoryRecord {
                thread: Some(thread),
                turn: None,
                item: None,
            });
        }
        let turns = sqlx::query("SELECT * FROM turns WHERE thread_id=? ORDER BY COALESCE(julianday(started_at),julianday(completed_at)),ordinal,id")
            .bind(thread_id)
            .fetch_all(&self.pool)
            .await?;
        for row in turns {
            let turn = HistoryTurnView {
                id: row.get("id"),
                thread_id: row.get("thread_id"),
                ordinal: row.get("ordinal"),
                status: row.get("status"),
                started_at: row.get("started_at"),
                completed_at: row.get("completed_at"),
            };
            let turn_id = turn.id.clone();
            records.push(HistoryRecord {
                thread: None,
                turn: Some(turn),
                item: None,
            });
            let items = sqlx::query(
                "SELECT * FROM items WHERE turn_id=? ORDER BY julianday(occurred_at),ordinal,id",
            )
            .bind(&turn_id)
            .fetch_all(&self.pool)
            .await?;
            for item in items {
                let (content_text, content_truncated) = item
                    .get::<Option<String>, _>("content_text")
                    .map(|value| truncate_utf8(value, 256 * 1024))
                    .map(|(value, truncated)| (Some(value), truncated))
                    .unwrap_or((None, false));
                let mut detail = item
                    .get::<Option<String>, _>("structured_detail")
                    .and_then(|value| serde_json::from_str::<Value>(&value).ok());
                let detail_truncated = detail
                    .as_ref()
                    .and_then(|value| serde_json::to_vec(value).ok())
                    .is_some_and(|value| value.len() > 256 * 1024);
                if detail_truncated {
                    detail = Some(serde_json::json!({"truncated":true}));
                }
                records.push(HistoryRecord {
                    thread: None,
                    turn: None,
                    item: Some(HistoryItemView {
                        id: item.get("id"),
                        turn_id: item.get("turn_id"),
                        ordinal: item.get("ordinal"),
                        kind: item.get("kind"),
                        status: item.get("status"),
                        revision: item.get("revision"),
                        content_text,
                        structured_detail: detail,
                        is_truncated: content_truncated || detail_truncated,
                        occurred_at: item.get("occurred_at"),
                        completed_at: item.get("completed_at"),
                        attachments: self.item_attachments(&item.get::<String, _>("id")).await?,
                    }),
                });
            }
        }
        Ok(records)
    }

    pub async fn receive_command(
        &self,
        queue_epoch: &str,
        sequence: i64,
        target_key: &str,
        priority: i64,
        command: &DeviceCommand,
    ) -> Result<InboxRecord> {
        let json = serde_json::to_string(command)?;
        let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
        sqlx::query("INSERT OR IGNORE INTO command_inbox(command_id,queue_epoch,server_sequence,target_key,priority,payload,status,received_at) VALUES(?,?,?,?,?,?,'accepted',?)")
            .bind(&command.command_id).bind(queue_epoch).bind(sequence).bind(target_key).bind(priority).bind(&json).bind(now()).execute(&mut *tx).await?;
        let row = sqlx::query("SELECT * FROM command_inbox WHERE command_id=?")
            .bind(&command.command_id)
            .fetch_optional(&mut *tx)
            .await?;
        let Some(row) = row else {
            let collision: Option<String> = sqlx::query_scalar(
                "SELECT command_id FROM command_inbox WHERE queue_epoch=? AND server_sequence=?",
            )
            .bind(queue_epoch)
            .bind(sequence)
            .fetch_optional(&mut *tx)
            .await?;
            anyhow::bail!(
                "command queue identity collision: epoch={queue_epoch} sequence={sequence} existing={}",
                collision.unwrap_or_else(|| "missing".into())
            );
        };
        if row.get::<String, _>("queue_epoch") != queue_epoch
            || row.get::<i64, _>("server_sequence") != sequence
            || row.get::<String, _>("payload") != json
        {
            anyhow::bail!("command id reused with different queue identity or payload")
        }
        let record = inbox_from_row(&row)?;
        tx.commit().await?;
        Ok(record)
    }

    pub async fn enqueue_local_command(
        &self,
        target_key: &str,
        priority: i64,
        command: &DeviceCommand,
    ) -> Result<InboxRecord> {
        let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
        let sequence: i64 = sqlx::query_scalar("INSERT INTO runtime_state(key,value,updated_at) VALUES('next_local_command_sequence','2',?) ON CONFLICT(key) DO UPDATE SET value=CAST(runtime_state.value AS INTEGER)+1,updated_at=excluded.updated_at RETURNING CAST(value AS INTEGER)-1")
            .bind(now()).fetch_one(&mut *tx).await?;
        let json = serde_json::to_string(command)?;
        sqlx::query("INSERT INTO command_inbox(command_id,queue_epoch,server_sequence,target_key,priority,payload,status,received_at) VALUES(?,'local',?,?,?,?, 'accepted',?)")
            .bind(&command.command_id).bind(sequence).bind(target_key).bind(priority).bind(json).bind(now()).execute(&mut *tx).await?;
        let row = sqlx::query("SELECT * FROM command_inbox WHERE command_id=?")
            .bind(&command.command_id)
            .fetch_one(&mut *tx)
            .await?;
        let record = inbox_from_row(&row)?;
        tx.commit().await?;
        Ok(record)
    }

