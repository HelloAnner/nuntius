    pub async fn record_user_turn(
        &self,
        thread_id: &str,
        app_turn_id: Option<&str>,
        text: &str,
    ) -> Result<String> {
        let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
        let ordinal: i64 =
            sqlx::query_scalar("SELECT COALESCE(MAX(ordinal),0)+1 FROM turns WHERE thread_id=?")
                .bind(thread_id)
                .fetch_one(&mut *tx)
                .await?;
        let turn_id = new_id("trn");
        let item_id = new_id("itm");
        let stamp = now();
        sqlx::query("INSERT INTO turns(id,thread_id,app_server_turn_id,ordinal,status,started_at) VALUES(?,?,?,?,'active',?)").bind(&turn_id).bind(thread_id).bind(app_turn_id).bind(ordinal).bind(&stamp).execute(&mut *tx).await?;
        sqlx::query("INSERT INTO items(id,turn_id,ordinal,kind,status,content_text,occurred_at,completed_at) VALUES(?, ?, 1, 'user_message', 'completed', ?, ?, ?)").bind(item_id).bind(&turn_id).bind(text).bind(&stamp).bind(&stamp).execute(&mut *tx).await?;
        sqlx::query(
            "UPDATE threads SET status='active',last_activity_at=?,updated_at=? WHERE id=?",
        )
        .bind(&stamp)
        .bind(&stamp)
        .bind(thread_id)
        .execute(&mut *tx)
        .await?;
        tx.commit().await?;
        Ok(turn_id)
    }
    pub async fn record_agent_message(
        &self,
        thread_id: &str,
        app_turn_id: Option<&str>,
        app_item_id: Option<&str>,
        text: &str,
        detail: &Value,
    ) -> Result<String> {
        let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
        let existing = if let Some(app_turn) = app_turn_id {
            sqlx::query_scalar::<_, String>(
                "SELECT id FROM turns WHERE thread_id=? AND app_server_turn_id=?",
            )
            .bind(thread_id)
            .bind(app_turn)
            .fetch_optional(&mut *tx)
            .await?
        } else {
            None
        };
        let turn_id = if let Some(id) = existing {
            id
        } else if app_turn_id.is_none() {
            // Events without an app turn id belong to the thread's current turn;
            // only synthesize a turn when the thread has none yet.
            if let Some(id) = sqlx::query_scalar::<_, String>(
                "SELECT id FROM turns WHERE thread_id=? ORDER BY ordinal DESC LIMIT 1",
            )
            .bind(thread_id)
            .fetch_optional(&mut *tx)
            .await?
            {
                id
            } else {
                let id = new_id("trn");
                sqlx::query("INSERT INTO turns(id,thread_id,app_server_turn_id,ordinal,status,started_at) VALUES(?,?,NULL,1,'active',?)").bind(&id).bind(thread_id).bind(now()).execute(&mut *tx).await?;
                id
            }
        } else {
            let ordinal: i64 = sqlx::query_scalar(
                "SELECT COALESCE(MAX(ordinal),0)+1 FROM turns WHERE thread_id=?",
            )
            .bind(thread_id)
            .fetch_one(&mut *tx)
            .await?;
            let id = new_id("trn");
            sqlx::query("INSERT INTO turns(id,thread_id,app_server_turn_id,ordinal,status,started_at) VALUES(?,?,?,?,'active',?)").bind(&id).bind(thread_id).bind(app_turn_id).bind(ordinal).bind(now()).execute(&mut *tx).await?;
            id
        };
        let ordinal: i64 =
            sqlx::query_scalar("SELECT COALESCE(MAX(ordinal),0)+1 FROM items WHERE turn_id=?")
                .bind(&turn_id)
                .fetch_one(&mut *tx)
                .await?;
        let item_id = new_id("itm");
        let stamp = now();
        sqlx::query("INSERT OR IGNORE INTO items(id,turn_id,app_server_item_id,ordinal,kind,status,content_text,structured_detail,occurred_at,completed_at) VALUES(?,?,?,?,'agent_message','completed',?,?,?,?)").bind(&item_id).bind(&turn_id).bind(app_item_id).bind(ordinal).bind(text).bind(serde_json::to_string(detail)?).bind(&stamp).bind(&stamp).execute(&mut *tx).await?;
        sqlx::query(
            "UPDATE threads SET status='active',last_activity_at=?,updated_at=? WHERE id=?",
        )
        .bind(&stamp)
        .bind(&stamp)
        .bind(thread_id)
        .execute(&mut *tx)
        .await?;
        tx.commit().await?;
        Ok(turn_id)
    }
    pub async fn complete_app_turn(
        &self,
        thread_id: &str,
        app_turn_id: Option<&str>,
        status: &str,
    ) -> Result<()> {
        let stamp = now();
        let mut tx = self.pool.begin_with("BEGIN IMMEDIATE").await?;
        if let Some(app_turn_id) = app_turn_id {
            sqlx::query("UPDATE turns SET status=?,completed_at=? WHERE thread_id=? AND app_server_turn_id=?")
                .bind(status).bind(&stamp).bind(thread_id).bind(app_turn_id).execute(&mut *tx).await?;
        } else {
            sqlx::query("UPDATE turns SET status=?,completed_at=? WHERE id=(SELECT id FROM turns WHERE thread_id=? ORDER BY ordinal DESC LIMIT 1)")
                .bind(status).bind(&stamp).bind(thread_id).execute(&mut *tx).await?;
        }
        sqlx::query("UPDATE threads SET status='idle',last_activity_at=?,updated_at=? WHERE id=?")
            .bind(&stamp)
            .bind(&stamp)
            .bind(thread_id)
            .execute(&mut *tx)
            .await?;
        tx.commit().await?;
        Ok(())
    }
    pub async fn import_app_thread(&self, project_id: &str, thread: &Value) -> Result<String> {
        let app_id = thread
            .get("id")
            .and_then(Value::as_str)
            .ok_or_else(|| anyhow!("App Server thread has no id"))?;
        if let Some(id) = self.local_thread_id(app_id).await? {
            self.update_imported_thread(&id, thread).await?;
            return Ok(id);
        }
        let id = new_id("thr");
        let title = imported_thread_title(thread);
        let stamp = unix_value_to_rfc3339(thread.get("updatedAt")).unwrap_or_else(now);
        let status = value_status(thread.get("status"), "idle");
        sqlx::query("INSERT INTO threads(id,project_id,app_server_thread_id,title,status,last_activity_at,created_at,updated_at) VALUES(?,?,?,?,?,?,?,?)")
            .bind(&id).bind(project_id).bind(app_id).bind(title).bind(status).bind(&stamp).bind(&stamp).bind(&stamp)
            .execute(&self.pool).await?;
        Ok(id)
    }
