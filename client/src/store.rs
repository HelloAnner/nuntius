    pub async fn state_get(&self, key: &str) -> Result<Option<String>> {
        Ok(
            sqlx::query_scalar("SELECT value FROM runtime_state WHERE key=?")
                .bind(key)
                .fetch_optional(&self.pool)
                .await?,
        )
    }
    pub async fn state_set(&self, key: &str, value: &str) -> Result<()> {
        sqlx::query("INSERT INTO runtime_state(key,value,updated_at) VALUES(?,?,?) ON CONFLICT(key) DO UPDATE SET value=excluded.value,updated_at=excluded.updated_at").bind(key).bind(value).bind(now()).execute(&self.pool).await?;
        Ok(())
    }
    pub async fn next_history_revision(&self, thread_id: &str) -> Result<i64> {
        let key = format!("history_revision:{thread_id}");
        let revision: String = sqlx::query_scalar("INSERT INTO runtime_state(key,value,updated_at) VALUES(?,'1',?) ON CONFLICT(key) DO UPDATE SET value=CAST(runtime_state.value AS INTEGER)+1,updated_at=excluded.updated_at RETURNING value")
            .bind(key).bind(now()).fetch_one(&self.pool).await?;
        Ok(revision.parse()?)
    }
    pub async fn active_command_queue_epoch(&self) -> Result<Option<String>> {
        self.state_get("active_command_queue_epoch").await
    }
    pub async fn last_server_sequence(&self, queue_epoch: Option<&str>) -> Result<i64> {
        let key = queue_epoch
            .map(|epoch| format!("last_server_sequence:{epoch}"))
            .unwrap_or_else(|| "last_server_sequence".into());
        Ok(self
            .state_get(&key)
            .await?
            .and_then(|v| v.parse().ok())
            .unwrap_or(0))
    }

